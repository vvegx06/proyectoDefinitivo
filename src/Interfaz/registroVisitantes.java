    private void RegistrarVisitanteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegistrarVisitanteActionPerformed
        String nombres = nombreTxt.getText();
        String edads = edadTXT.getText();
        String fecha = FechaTxt.getText();
        if (nombres.isEmpty() || edads.isEmpty() || fecha.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Error, complete todos los campos");
        } else if (str(nombres) && ent(edads) && ent(fecha)) {
            int edades = Integer.parseInt(edads);
            int fechas = Integer.parseInt(fecha);

            if (contador < nombre.length) {
                nombre[contador] = nombres;
                edad[contador] = edades;
                fechases[contador] = fechas;

                contador++;

                nombreTxt.setText("");
                edadTXT.setText("");
                FechaTxt.setText("");

                JOptionPane.showMessageDialog(null, "Datos guardados correctamente");
            } else {
                JOptionPane.showMessageDialog(null, "Error, No hay espacio para mÃ¡s animales");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Error, no se permiten los datos ingresados ");
    }//GEN-LAST:event_RegistrarVisitanteActionPerformed

    }

    private void imprimir() {
        if (contador == 0) {
            JOptionPane.showMessageDialog(null, "No hay visitantes en el registro");
        } else {
            String mensaje = "";
            for (int i = 0; i < contador; i++) {
                mensaje += "Visitantes: " + nombre[i] + "\n";
                mensaje += "Nombre : " + nombre[i] + "\n";
                mensaje += "edad : " + edad[i] + "\n";
                mensaje += "Fecha: " + fechases[i] + "\n";

            }
            JOptionPane.showMessageDialog(null, mensaje);
        }

    }

    private boolean str(String valor) {
        return valor.matches("[a-zA-Z]+");
    }
    private boolean fechas(String fechas) {
        return fechas.matches("\\d{2}/\\d{2}/\\d{4}");
    }
    
    private boolean ent(String valor) {
            if (valor.matches("\\d+")) {
                return true;
            } else {
                return false;
            }
    }
    private void verVisitante1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_verVisitante1ActionPerformed
        imprimir();
    }//GEN-LAST:event_verVisitante1ActionPerformed

